sum <- function(first, second){
  return (first + second)
}
 
subtract <- function(x, y) {
  return(x - y)
}
  
multiply <- function(first, second){
  return (first * second)
}

divide <- function(first, second){
  return (first / second)
}

exponent <- function(x,y){
  return (x^y)
}

square <- function(x){ 
  return(x ^ 2)
}

squareroot <- function(x){
  return(x**(1/2.0))
}

cube <- function(x){
  return(x**3)
}

cos <- function(x){
  return (pi*x)
}

tan <- function(x){
  return (pi*x)
}

sin <- function(x){
  return(pi*x)
}

